package oopOdev01;

public class Koenigsegg extends Cars {

private String limited;
private int speed;
private String isAHyperCar;

    public String getLimited() {
        return limited;
    }

    public void setLimited(String limited) {
        this.limited = limited;
    }

    public int getSpeed() {
        return speed;
    }

    public void setSpeed(int speed) {
        this.speed = speed;
    }

    public String getIsAHyperCar() {
        return isAHyperCar;
    }

    public void setIsAHyperCar(String isAHyperCar) {
        this.isAHyperCar = isAHyperCar;
    }
}
